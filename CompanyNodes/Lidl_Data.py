           "## Historie:\n"
           "Die Historie von Lidl begann im baden-württembergischen Neckarsulm, wo in den 1930er Jahren die Lidl & "
           "Schwarz KG als Lebensmittel-Sortimentsgroßhandlung gegründet wurde. Dies legte den Grundstein für die "
           "spätere internationale Expansion des Unternehmens. Ein herausragendes Merkmal in der Geschichte von Lidl "
           "ist der stetige Mut, neue Wege zu gehen und Impulse für die gesamte Branche zu setzen.\n\n "
           "Durch das erfolgreiche Discount-Prinzip konnte Lidl rasch wachsen, und bereits nach 15 Jahren existierten "
           "über 450 Filialen in Deutschland. In den späten 1980er Jahren begann die Expansion in andere Länder. "
           "Heute betreibt Lidl in 31 Ländern weltweit über 12.200 Filialen und setzt weiterhin auf Wachstum. Seit "
           "2017 und 2018 ist Lidl auch in den USA und in Serbien mit eigenen Filialen vertreten.\n "
           "\n"
           "Die Erfolgsgeschichte von Lidl zeigt Meilensteine wie die Einführung des Pfandsystems für Einwegflaschen "
           "im Jahr 2002 und die Integration einer eigenen Fairtrade-Marke namens \"Fairglobe\" im Jahr 2006. Der "
           "Lidl-Onlineshop ging 2008 an den Start, und 2017 wagte das Unternehmen den Sprung über den Atlantik, "
           "indem es die ersten Filialen in den Vereinigten Staaten eröffnete. In den folgenden Jahren wurde Lidl "
           "mehrfach für sein nachhaltiges Engagement ausgezeichnet, unter anderem mit dem Fairtrade-Award im Jahr "
           "2016. Die kontinuierliche Weiterentwicklung zeigt sich auch in der Veröffentlichung des ersten "
           "Nachhaltigkeitsberichts nach internationalem GRI-Standard im Jahr 2018 und der Kooperation mit Bioland im "
           "gleichen Jahr. Im Jahr 2019 veröffentlichte Lidl seine Nachhaltigkeitsstrategie 2030, die das Engagement "
           "des Unternehmens für eine nachhaltige Zukunft unterstreicht. [2]\n "
           "\n"

