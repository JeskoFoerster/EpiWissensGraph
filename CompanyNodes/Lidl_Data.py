           "Lidl, als nachhaltiger Frische-Discounter, steht für höchste Qualität zum besten Preis und bietet ein "
           "schnelles, einfaches sowie zuverlässiges Einkaufserlebnis. Die Unternehmensphilosophie betont Fairness, "
           "Kommunikation auf Augenhöhe und das Streben danach, jederzeit die erste Wahl für Kunden, Mitarbeiter und "
           "Partner zu sein. Das Geschäft wird systematisch, einfach und unkompliziert betrieben, wodurch Lidl ein "
           "herausragendes Preis-Leistungsverhältnis bietet. Als hybride Marke ist Lidl nah am Kunden und kombiniert "
           "ein umfangreiches Food- und Non-Food-Sortiment sowohl online als auch in stationären Geschäften. "
           "Das Unternehmen handelt bewusst im Sinne von Mensch und Natur und setzt sich kontinuierlich für "
           "Verbesserungen in den Bereichen Umwelt- und Klimaschutz, Mitarbeiter, gesellschaftliches Engagement und "
           "Sortiment ein. Der Erfolg von Lidl basiert auf dem unermüdlichen Engagement aller Mitarbeiter, "
           "die gemäß dem Motto \"Lidl muss man können\" täglich daran arbeiten, das zentrale Versprechen \"Lidl "
           "lohnt sich\" einzulösen. [1]\n "
