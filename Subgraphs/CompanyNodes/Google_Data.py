"## Historie:\n"
"Google wurde 1998 von Larry Page und Sergey Brin gegründet, während sie an der Stanford University in Kalifornien promovierten [1]. Sie entwickelten eine Suchmaschine namens “BackRub”, die Links verwendete, um die Bedeutung einzelner Seiten im World Wide Web zu bestimmen. Bald darauf wurde BackRub in Google umbenannt. Der Name war ein Wortspiel mit der mathematischen Bezeichnung für die Zahl 1, gefolgt von 100 Nullen, und spiegelte die Mission von Page und Brin wider, die Informationen der Welt zu organisieren und für alle zugänglich und nützlich zu machen [1]. Im August 1998 schrieb Andy Bechtolsheim, Mitbegründer von Sun Microsystems, einen Scheck über 100.000 $ aus, und Google Inc. wurde offiziell gegründet. Mit dieser Investition zog das neu gegründete Team von den Schlafsälen in ihr erstes Büro: eine Garage in Menlo Park, Kalifornien. Google wuchs schnell und zog schließlich in seinen jetzigen Hauptsitz, den Googleplex in Mountain View, Kalifornien [1] .\n"
