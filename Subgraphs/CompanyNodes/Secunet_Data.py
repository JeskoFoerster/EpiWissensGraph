"## Beschreibung:\n"
"Die Secunet Security Networks AG ist ein börsennotiertes IT-Unternehmen mit Hauptsitz in Essen, Deutschland. Das Unternehmen bietet seinen Kunden Lösungen, Produkte und Dienstleistungen im Bereich der IT-Sicherheit [1]. Mit über 1.000 Expert*innen sorgt Secunet für widerstandsfähige, digitale Infrastrukturen und den höchstmöglichen Schutz für Daten, Anwendungen und Identitäten. Secunet steht für digitale Souveränität und ist spezialisiert auf alle Bereiche mit besonderen Sicherheitsanforderungen – wie z. B. Cloud, IIoT, eGovernment, Biometrie und eHealth [2].\n"
"\n"
"## Historie:\n"
"Die Geschichte der Secunet Security Networks AG beginnt mit dem Aufkommen des World Wide Web in den 1990er Jahren. Die TÜV Mitte AG in Essen hatte ihren Tätigkeitsbereich um die Überprüfung der Sicherheit von Netzwerken erweitert. Da immer mehr Unternehmen ihre Netze prüfen und absichern lassen wollten, gliederte die TÜV Mitte AG ihre Abteilung für IT-Sicherheit 1997 unter dem Namen SecuNet Security Networks in eine GmbH aus[1]. Im November 1999 ging das Unternehmen an die Börse und firmierte fortan unter secunet Security Networks AG. Das Unternehmen ist seitdem im Prime Standard der Deutschen Börse gelistet.\n"
"\n"
"## Mission/Ziel:\n"
