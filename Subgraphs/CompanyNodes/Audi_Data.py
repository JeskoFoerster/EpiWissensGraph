"## Historie:\n"
"Die Geschichte der Audi AG reicht bis ins 19. Jahrhundert zurück. August Horch gründete 1899 sein erstes Automobilunternehmen, August Horch & Cie, in Köln. Kurz darauf, im Jahr 1909, gründete er ein neues Automobilunternehmen in Zwickau, das seit 1910 unter dem Namen Audiwerke AG, Zwickau, firmiert. Nach dem Zweiten Weltkrieg wurde 1949 eine neue Auto Union GmbH in Ingolstadt (Bayern) gegründet[3]. Dort liefen, neben DKW-Motorrädern, zunächst Schnellaster vom Band und ab 1950 baute das neue Werk Düsseldorf-Derendorf die F-89-Pkw, beides Modelle mit Frontantrieb [2]. Im Laufe der Jahre hat Audi eine Reihe von Meilensteinen erreicht. 1980 führte Audi den Quattro-Allradantrieb ein, eine Technologie, die das Fahren unter schwierigen Bedingungen revolutionierte.\n"
