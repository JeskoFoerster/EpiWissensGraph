"## Mission/Ziel:\n"
"Die Unternehmensstrategie von Audi, “Vorsprung 2030”, zielt darauf ab, die Zukunftsfähigkeit von Audi zu sichern und die Transformation in ein neues Mobilitätszeitalter maßgeblich mitzugestalten [5]. Audi CEO Markus Duesmann betont, dass das Tempo der Veränderungen in unserer Gesellschaft rasant zunimmt, daher beschleunigt Audi seine Transformation [4].\n"
