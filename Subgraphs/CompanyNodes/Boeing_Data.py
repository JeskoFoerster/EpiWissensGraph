"## Historie:\n"
"Boeing ist ein US-amerikanisches Unternehmen, das sich auf die Entwicklung, Herstellung und den Verkauf von Flugzeugen, Raketen und Satelliten spezialisiert hat. Das Unternehmen ist der größte Flugzeughersteller der Welt und hat seinen Hauptsitz in Chicago, Illinois. Boeing hat im Laufe seiner Geschichte viele Meilensteine erreicht."

"Im Jahr 1916 gründete William E. Boeing die Pacific Aero Products Company, die später in Boeing Airline Company umbenannt wurde. Das Unternehmen produzierte das erste Flugzeug, das in den USA für den kommerziellen Passagierverkehr zugelassen wurde."

"Im Jahr 1933 stellte Boeing das Modell 247 vor, das erste moderne Verkehrsflugzeug mit einer vollständig aus Metall gefertigten Karosserie."

"Im Jahr 1954 führte Boeing das erste strahlgetriebene Verkehrsflugzeug, die Boeing 707, ein."

"Im Jahr 1969 startete die Boeing 747, das erste Großraumflugzeug der Welt, zum ersten Mal."

"Im Jahr 1988 führte Boeing die Boeing 747-400 ein, die erste Version des Flugzeugs mit einem Glascockpit.\n"
